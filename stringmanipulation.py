# Basic concepts of string manipulation

text = "Hello, world!"
print(text)

# Extract a substring
substring = text[0:5]
print(substring)

# Replace a substring
new_text = text.replace("world", "Python")
print(new_text)

# Concatenate two strings
text1 = "Hello"
text2 = "Python"
new_text = text1 + " " + text2
print(new_text)
